export function getAyatKumulatif(surahNum: number, ayatNum: number): number | null {
  if (surahNum < 1 || surahNum > 114) return null;
  const surah = SURAH_DATA[surahNum - 1];
  if (ayatNum < 1 || ayatNum > surah[2]) return null;
  return surah[3] + ayatNum;
}

export function getSurahFromKumulatif(kumulatif: number): { name: string; ayat: number; number: number } {
  for (const surah of SURAH_DATA) {
    if (kumulatif <= surah[3] + surah[2]) {
      return {
        name: surah[1],
        ayat: kumulatif - surah[3],
        number: surah[0],
      };
    }
  }
  return { name: "An-Nas", ayat: 6, number: 114 };
}
